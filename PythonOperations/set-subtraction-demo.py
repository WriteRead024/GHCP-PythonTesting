# demo of set subtraction operation

set1 = {"apple", "banana", "orange", "grape"}
set2 = {"banana", "grape", "kiwi", "mango"}

result1 = set1 - set2
result2 = set2 - set1

print("Elements in set1 but not in set2:", result1)
print("Elements in set2 but not in set1:", result2)